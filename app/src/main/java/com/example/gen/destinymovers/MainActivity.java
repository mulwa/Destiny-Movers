public class MainActivity extends AppCompatActivity
        implements NavigationView.OnNavigationItemSelectedListener {
    private FragmentManager manager;
    private FirebaseAuth mAuth;
    private FirebaseUser currentUser;
    private FirebaseDatabase database;
    private DatabaseReference customerRef;
    private FirebaseAuth.AuthStateListener authStateListener;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);

        mAuth  = FirebaseAuth.getInstance();
        manager =  getSupportFragmentManager();

        authStateListener = new FirebaseAuth.AuthStateListener() {
            @Override
            public void onAuthStateChanged(@NonNull FirebaseAuth firebaseAuth) {
                currentUser = firebaseAuth.getCurrentUser();

                if(currentUser  == null){
//                    login login = new login();
//                    FragmentTransaction transaction = manager.beginTransaction();
//                    transaction.replace(R.id.mainLayout,login,"loginFragment");
//                    transaction.commit();

                }

            }
        };


        Trucks trucks = new Trucks();
        FragmentTransaction transaction = manager.beginTransaction();
        transaction.replace(R.id.mainLayout,trucks,"truckFragment");
        transaction.commit();




        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        ActionBarDrawerToggle toggle = new ActionBarDrawerToggle(
                this, drawer, toolbar, R.string.navigation_drawer_open, R.string.navigation_drawer_close);
        drawer.addDrawerListener(toggle);
        toggle.syncState();

        NavigationView navigationView = (NavigationView) findViewById(R.id.nav_view);
        navigationView.setNavigationItemSelectedListener(this);
    }

    @Override
    protected void onStart() {
        super.onStart();
        mAuth.addAuthStateListener(authStateListener);
    }

    @Override
    protected void onStop() {
        super.onStop();
        if(authStateListener != null){
            mAuth.removeAuthStateListener(authStateListener);
        }
    }

    @Override
    public void onBackPressed() {
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        if (drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.closeDrawer(GravityCompat.START);
        } else {
            super.onBackPressed();
        }
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.main, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
        if (id == R.id.action_settings) {
            return true;
        }

        return super.onOptionsItemSelected(item);
    }

    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();
        if(id == R.id.nav_request_transport){
            Trucks trucks = new Trucks();
            FragmentTransaction transaction = manager.beginTransaction();
            transaction.replace(R.id.mainLayout,trucks,"truckFragment");
            transaction.commit();
        }else if (id == R.id.nav_sign_up){
            signUp sign = new signUp();
            FragmentTransaction transaction = manager.beginTransaction();
            transaction.replace(R.id.mainLayout,sign,"eventFragment");
            transaction.commit();
        }else if(id == R.id.nav_login){
            login login = new login();
            FragmentTransaction transaction = manager.beginTransaction();
            transaction.replace(R.id.mainLayout,login,"eventFragment");
            transaction.commit();
        }else if(id == R.id.nav_my_request){
            MyRequestFragment myRequestFragment= new MyRequestFragment();
            FragmentTransaction transaction = manager.beginTransaction();
            transaction.replace(R.id.mainLayout,myRequestFragment,"myRequestFragment");
            transaction.commit();

        }


        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }
}
